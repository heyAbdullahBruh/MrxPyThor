# 2) Rectangle shape's Area calculate--> 

'''
width = float(input("Enter Rectangle's width :"))
length = float(input("Enter Rectangle's length :"))

areaOfRectangle = 2*(width+length)
print("The area of Rectangle Shape is :- ",areaOfRectangle)
'''


# 3) Circle shape's Area calculate--> 
'''
breadth = float(input("Enter Circle's breadth :"))

areaOfCircle = 3.1416 * breadth **2 
print("The area of Circle Shape is :- ",areaOfCircle) 
'''


